void AWS_PlayerControll::CheckTimeSync(float DeltaTime)
{
	// 1. 计时器累加
	// TimeSyncRunningTime 是一个计时器变量，每一帧都会加上流逝的时间 (DeltaTime)。
	// 就像一个秒表在不停地走。
	TimeSyncRunningTime += DeltaTime;

	
	// 2. 条件检查
	// 这个 if 语句有两个条件，必须同时满足才会执行里面的代码：
	if (IsLocalController() && TimeSyncRunningTime > TimeSyncFrequency)
	{
		// 条件一: IsLocalController()
		// 确保这段代码只在玩家自己的电脑上（客户端）运行，而不是在服务器上为其他玩家的傀儡Controller运行。
		// 时间同步是由客户端主动发起的。

		// 条件二: TimeSyncRunningTime > TimeSyncFrequency
		// 检查秒表的时间是否已经超过了我们设定的同步频率（比如 TimeSyncFrequency = 5.0f）。
		// 相当于闹钟响了：“已经过去5秒了！”

		// 3. 发起请求
		// 这是最关键的一步。它调用了一个 RPC (远程过程调用)，向服务器发送请求。
		// 告诉服务器：“你好，请告诉我你现在的时间是多少？顺便一提，我是在我这里的这个时间点(GetWorld()->GetTimeSeconds())给你发的消息。”
		ServerRequestServerTime(GetWorld()->GetTimeSeconds());
		// 4. 重置计时器
		// 请求发送出去后，立刻将秒表清零，准备开始下一个5秒的倒计时。
		TimeSyncRunningTime = 0.f;
	}
}

void AWS_PlayerControll::HighPingWarning()
{
	BlasterHUD = BlasterHUD == nullptr ? Cast<ABlasterHUD>(GetHUD()) : BlasterHUD;
	bool bHUDValid = BlasterHUD &&
		BlasterHUD->CharacterOverlay &&
		BlasterHUD->CharacterOverlay->HighPingImage &&
		BlasterHUD->CharacterOverlay->HighPingAnimation;
	if (bHUDValid)
	{
		BlasterHUD->CharacterOverlay->HighPingImage->SetOpacity(1.f);
		BlasterHUD->CharacterOverlay->PlayAnimation(
			BlasterHUD->CharacterOverlay->HighPingAnimation,
			0.f,
			5);
	}
}

void AWS_PlayerControll::StopHighPingWarning()
{
	BlasterHUD = BlasterHUD == nullptr ? Cast<ABlasterHUD>(GetHUD()) : BlasterHUD;
	bool bHUDValid = BlasterHUD &&
		BlasterHUD->CharacterOverlay &&
		BlasterHUD->CharacterOverlay->HighPingImage &&
		BlasterHUD->CharacterOverlay->HighPingAnimation;
	if (bHUDValid)
	{
		BlasterHUD->CharacterOverlay->HighPingImage->SetOpacity(0.f);
		if (BlasterHUD->CharacterOverlay->IsAnimationPlaying(BlasterHUD->CharacterOverlay->HighPingAnimation))
		{
			BlasterHUD->CharacterOverlay->StopAnimation(BlasterHUD->CharacterOverlay->HighPingAnimation);
		}
	}
}

void AWS_PlayerControll::CheckPing(float DeltaTime)
{
	if (HasAuthority()) return;
	HighPingRunningTime += DeltaTime;
	if (HighPingRunningTime > CheckPingFrequency)
	{
		PlayerState = PlayerState == nullptr ? TObjectPtr<APlayerState>(GetPlayerState<APlayerState>()) : PlayerState;
		if (PlayerState)
		{
			// Replace GetPing() by GetPingInMilliseconds()
			// If Replace GetPing() by GetPingInMilliseconds(), I think that don't need "* 4"？
			if (PlayerState-> GetPingInMilliseconds() * 4 > HighPingThreshold) // ping is compressed; it's actually ping / 4
			{
				HighPingWarning();
				PingAnimationRunningTime = 0.f;
				ServerReportPingStatus(true);
			}
			else
			{
				ServerReportPingStatus(false);
			}
		}
		HighPingRunningTime = 0.f;
	}
	bool bHighPingAnimationPlaying =
		BlasterHUD && BlasterHUD->CharacterOverlay &&
		BlasterHUD->CharacterOverlay->HighPingAnimation &&
		BlasterHUD->CharacterOverlay->IsAnimationPlaying(BlasterHUD->CharacterOverlay->HighPingAnimation);
	if (bHighPingAnimationPlaying)
	{
		PingAnimationRunningTime += DeltaTime;
		if (PingAnimationRunningTime > HighPingDuration)
		{
			StopHighPingWarning();
		}
	}
}

void AWS_PlayerControll::ShowReturnToMainMenu()
{
	if (ReturnToMainMenuWidget == nullptr) return;
	if (ReturnToMainMenu == nullptr)
	{
		ReturnToMainMenu = CreateWidget<UReturnToMainMenu>(this, ReturnToMainMenuWidget);
	}
	if (ReturnToMainMenu)
	{
		bReturnToMainMenuOpen = !bReturnToMainMenuOpen;
		if (bReturnToMainMenuOpen)
		{
			ReturnToMainMenu->MenuSetup();
		}
		else
		{
			ReturnToMainMenu->MenuTearDown();
		}
	}
}

void AWS_PlayerControll::ServerCheckMatchState_Implementation()
{
	ABlasterGamemode* GameMode = Cast<ABlasterGamemode>(UGameplayStatics::GetGameMode(this));
	if (GameMode)
	{
		WarmupTime = GameMode->WarmupTime;
		MatchTime = GameMode-> MatchTime;
		CooldownTime = GameMode-> CooldownTime;
		LevelStartingTime = GameMode->LevelStartingTime;
		MatchState = GameMode->GetMatchState();
		ClientJoinMidgame(MatchState, WarmupTime, MatchTime, CooldownTime,LevelStartingTime);
	}
}

void AWS_PlayerControll::ClientJoinMidgame_Implementation(FName StateOfMatch, float Warmup, float Match,float Cooldown,float StartingTime)
{
	WarmupTime = Warmup;
	MatchTime = Match;
	CooldownTime = Cooldown;
	LevelStartingTime = StartingTime;
	MatchState = StateOfMatch;
	OnMatchStateSet(MatchState);
	if (BlasterHUD && MatchState == MatchState::WaitingToStart)
	{
		BlasterHUD->AddAnnouncement();
	}
}

