void AWS_PlayerControll::BroadcastElim(APlayerState* Attacker, APlayerState* Victim)
{
	APlayerState* Self = GetPlayerState<APlayerState>();
	if (Attacker && Victim && Self)
	{
		BlasterHUD = BlasterHUD == nullptr ? Cast<ABlasterHUD>(GetHUD()) : BlasterHUD;
		if (BlasterHUD)
		{
			if (Attacker == Self && Victim != Self)
			{
				BlasterHUD->AddElimAnnouncement("You", Victim->GetPlayerName());
				return;
			}
			if (Victim == Self && Attacker != Self)
			{
				BlasterHUD->AddElimAnnouncement(Attacker->GetPlayerName(), "you");
				return;
			}
			if (Attacker == Victim && Attacker == Self)
			{
				BlasterHUD->AddElimAnnouncement("You", "yourself");
				return;
			}
			if (Attacker == Victim && Attacker != Self)
			{
				BlasterHUD->AddElimAnnouncement(Attacker->GetPlayerName(), "themselves");
				return;
			}
			BlasterHUD->AddElimAnnouncement(Attacker->GetPlayerName(), Victim->GetPlayerName());
		}
	}
}

void AWS_PlayerControll::BeginPlay()
{
	Super::BeginPlay();

	// 对于服务器玩家（Listen Server），Pawn在BeginPlay时就已经有了，但OnRep_Pawn不会被调用
	// 所以我们需要在这里为服务器玩家也添加一次输入
	// IsLocalController()确保只在本地控制的Controller上执行
	if (IsLocalController() && !bMappingContextAdded)
	{
		if (UEnhancedInputLocalPlayerSubsystem* Subsystem = ULocalPlayer::GetSubsystem<UEnhancedInputLocalPlayerSubsystem>(GetLocalPlayer()))
		{
			if (AWS_Player* WS_Player = Cast<AWS_Player>(GetPawn()))
			{
				if (WS_Player->GetDefaultMappingContext())
				{
					Subsystem->AddMappingContext(WS_Player->GetDefaultMappingContext(), 0);
					UE_LOG(LogTemp, Warning, TEXT("SERVER/LOCAL: Input Mapping Context Added on BeginPlay"));
					bMappingContextAdded = true;
				}
			}
		}
	}

	BlasterHUD = Cast<ABlasterHUD>(GetHUD());
	ServerCheckMatchState();
}

